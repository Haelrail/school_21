#include "push_swap.h"

void	lst_clear(t_list **lst, int i)
{
	t_list	*elem;

	while (i)
	{
		elem = (*lst)-> next;
		(*lst)-> next = elem -> next;
		free (elem);
		i--;
	}
	*lst = 0;
}
